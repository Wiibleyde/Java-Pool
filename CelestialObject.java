public class CelestialObject {
    // x (double) y (double) z (double) name (String)
    private double x;
    private double y;
    private double z;
    private String name;
    public CelestialObject(double x, double y, double z, String name) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.name = name;
    }
}
